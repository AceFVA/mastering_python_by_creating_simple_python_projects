# Create a program that print all the odd numbers starting from 0 to 100. (Use while loop) 

# set a variable to an initial value of 0
num = 0

# while num is less than or equal to 100, enter loop
while num <= 100:
# if num % 2 is not equal to 0, then the number is odd
    if num % 2 != 0:
# print num
        print(num)
# num = num + 1
        num += 1

# when the number is even, num = num +1
    num += 1
